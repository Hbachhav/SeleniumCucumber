package Practice;

public class swapNo {

    public static void main(String[] args) {
        int a=10;
        int b=20;

        System.out.println("no before swap" +a );
        System.out.println("no before swap" +b );

        int temp;

        temp = a;
        a = b;
        b= temp;

        System.out.println("no After swap" +a);
        System.out.println("no After swap" +b);





    }
}
